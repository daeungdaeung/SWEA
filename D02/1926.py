T = int(input())
# 여러개의 테스트 케이스가 주어지므로, 각각을 처리합니다.
s = ''
for test_case in range(1, T + 1):
    # ///////////////////////////////////////////////////////////////////////////////////
    cnt = 0
    for num in list(str(test_case)):
        if num in ['3', '6', '9']:
            cnt += 1
    if cnt:
        s += '-'*cnt + ' '
    else:
        s += str(test_case) + ' '

print(s)
